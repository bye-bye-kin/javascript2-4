// インクリメント、デクリメント
// x++  →　　x=x+1
// x--  →　　x=x-1

// 前置演算
var x = 3;
var y = x++;
console.log(x);
console.log(y);
// 後置演算
var x = 3;
var y = ++x;
console.log(x);
console.log(y);
